    private void btnOpenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOpenActionPerformed
        int caracter=0;
        
         FileReader archivoTexto;
        try {
           archivoTexto = new FileReader(txtNombreArchivo.getText());
           txtContenido.setText("");
           while((caracter=archivoTexto.read())!=-1)
               txtContenido.setText(txtContenido.getText()+(char)caracter);
           
           archivoTexto.close();
            
          } catch (IOException ex){
            
       }
    }//GEN-LAST:event_btnOpenActionPerformed

